    <h1>Laravel Folder Structure Explorer</h1>
    <h3>app:</h3>
        <p> The app folder contains the core code of the application such as Http, Models and providers</p>
    
    <h3>routes:</h3>
        <p> A Laravel route is a way to define how your application responds to specific HTTP requests</p>
    
    <h3>resources:</h3>
        <p> The resources folder contains Blade view files, CSS, and JavaScript used for the user interface. In Laravel, a Resource file is a way to transform data before sending it to the user. Think of it like a translator.</p>
    
    <h3>public:</h3>
        <p>The public directory contains the index.php file, which is the entry point for all requests entering your application and configures autoloading.</p>
    
    <h3>config:</h3>
        <p>The config folder stores configuration files for the application such as database, mail, and app settings.</p>
